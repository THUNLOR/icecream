        <h1 style="color: yellow;font-family: 'Times New Roman', Times, serif;font-weight: bold;font-size: 200%;text-align: left;">Story Line :</h1>
        <br>
        <br>
        <p style="color: white;font-family: 'Times New Roman', Times, serif;font-size: 150%;font-weight: bold;text-align: center;">A joint operation between the United States Marshals Service and the county sheriff's department—consisting of Deputy Marshal Cameron Burke (Doug Hutchison), Sheriff Earl Whitehorse (Christopher Heyerdahl) and his deputies Joey Hudson (Luisa D'Oliveira), Staci Pratt (Julian Bailey) and the player character, an unnamed junior Deputy—arrive at an Eden's Gate church to detain Joseph Seed with a federal arrest warrant on charges of kidnapping with the intent to harm. Although Joseph offers no resistance, he claims that God will not allow him to be taken. As he is escorted away, cult members attack the task force and cause their helicopter to crash. Joseph escapes and commands the cult to capture the task force members.[12]

            The Deputy survives and escapes with the help of Dutch, who explains that the cult has interpreted the attempt to arrest Joseph as the fulfillment of his prophecies. Believing the end of the world is upon them, they have begun "the Reaping", a concentrated effort to purge Hope County of non-believers and gather all material needs. As the county is now cut off from the outside world, Dutch tasks the Deputy with contacting the fledgling resistance scattered throughout Hope County to gather allies, rescue the task force and dismantle the cult's operations to erode the influence of Joseph and the Heralds. Although the Heralds focus their efforts in stopping the Deputy, they maintain an interest in recruiting them to their cause.
            
            Joseph's younger brother John lures the Deputy to Holland Valley by broadcasting a message showing Hudson as his captive. John indoctrinates residents to the cult's beliefs by subjecting them to a forced baptism to "cleanse" them, tattooing their "sins" onto their bodies, and carving out the marked skin as part of their "atonement". John captures the Deputy and concludes that their sin is Wrath. When the Deputy escapes, John retaliates by taking over the town of Fall's End where he forces the Deputy to confront him and atone for their sins. A counterattack by the local Resistance prompts John to flee, leading to the Deputy engaging him in an aerial dogfight and mortally wounding him. As he lays dying, John remarks that the Deputy will not save anyone and asserts that Joseph is right. The Deputy rescues Hudson and the captive survivors and destroys John's bunker.
            
            The Deputy arrives in the Whitetail Mountains to find the Whitetail Militia already at war with Joseph's older brother Jacob. Jacob recruits and trains the cult's members and keeps an indoctrinated Deputy Pratt captive. As part of their training, Jacob mentally conditions cult recruits with The Platters' song "Only You (And You Alone)", which he uses to trigger an altered state of consciousness in the recruits and carry out his commands. Jacob subjects the Deputy to the same conditioning and successfully breaks them. He has the Deputy kill militia leader Eli Palmer (Patrick Garrow) to cripple the resistance. The surviving militia hold Jacob responsible and the Deputy confronts him in the mountains. As he dies, Jacob reflects on how empires rise and fall, and predicts that America will suffer the same fate if it is not prepared. The Deputy retrieves Jacob's bunker key, rescues Pratt and floods the bunker.
            
            Faith, a collaborator and "sister" of the Seeds, oversees the production of Bliss in Henbane River. The hallucinatory effects of Bliss puts its victims in a trance-like state and staying under its influence for too long makes them vulnerable to Faith's hypnotic manipulation. The Deputy reunites with Whitehorse and joins the Cougars, based in the Hope County Jail. Whitehorse informs the Deputy that Burke has been captured and is now under the influence of the Bliss. The Deputy repeatedly encounters Faith, who attempts to persuade the Deputy to join her and live in paradise. The Deputy resists her and rescues Burke, unaware that he has been hypnotized. Burke kills Virgil Minkler, leader of the Cougars and Mayor of Fall's End, opens the jail to Eden's Gate and kills himself. The Cougars suffer heavy losses repelling the cult and Whitehorse is captured. The Deputy kills Faith, and she warns them that Joseph is right about the end of the world, but the Deputy will decide what happens in the end. The Deputy rescues Whitehorse from Faith's bunker and destroys the production of Bliss.
            
            Killing his Heralds prompts Joseph to call the Deputy for a final confrontation at his church. He has captured the Deputy's allies from the resistance groups, hypnotized them with Bliss and is holding Whitehorse, Hudson and Pratt hostage. Joseph offers the Deputy the option of walking away with their friends unharmed on the condition that they leave him and Eden's Gate alone.[15]</p>
